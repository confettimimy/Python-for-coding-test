# 1. '문자열'의 구성이 알파벳인지 확인하기(isalpha)

Ex1 = 'A'
 
Ex2 = 'ABC'
 
Ex3 = "앱피아"
 
Ex4 = "Hello Appia"
 
Ex5 = "100Appia"

print(Ex1.isalpha())
 
print(Ex2.isalpha())
 
print(Ex3.isalpha())
 
print(Ex4.isalpha())
 
print(Ex5.isalpha())



# 2. '문자열'의 구성이 숫자인지 확인하기(isdigit)

Ex1 = '010-1234-5678'
 
Ex2 = '123456'
 
Ex3 = "R4R3"
 
print(Ex1.isdigit())
 
print(Ex2.isdigit())
 
print(Ex3.isdigit())
